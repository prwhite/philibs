void pnidbgstringonetime ( std::string const& str )
{
  using DbgStrs = std::unordered_set<std::string>;
  static DbgStrs sDbgStrs;

  if ( sDbgStrs.find(str) == sDbgStrs.end())
  {
    sDbgStrs.insert(str);
    std::cout << str << std::endl;
  }
}

