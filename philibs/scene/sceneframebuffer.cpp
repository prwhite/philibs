// overrides from pni::pstd::refCount

void framebuffer::collectRefs ( pni::pstd::refCount::Refs& refs )
{
  for(auto iter : mColorTex)
    refs.push_back(iter.get());

  refs.push_back(mDepthTex.get());
  refs.push_back(mStencilTex.get());
}

